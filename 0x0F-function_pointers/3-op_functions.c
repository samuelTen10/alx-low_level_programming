#include "3-calc.h"

/**
 * op_add - adds integers
 *
 * @a: first int
 * @b: second int
 *
 * Return: Always 0.
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtructs integers
 *
 * @a: first int
 * @b: second int
 *
 * Return: Always 0.
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - adds integers
 *
 * @a: first int
 * @b: second int
 *
 * Return: Always 0.
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - adds integers
 *
 * @a: first int
 * @b: second int
 *
 * Return: Always 0.
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - adds integers
 *
 * @a: first int
 * @b: second int
 *
 * Return: Always 0.
*/

int op_mod(int a, int b)
{
	return (a % b);
}
